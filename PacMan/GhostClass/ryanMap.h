public:
    int pxPerBlock = 24;

    int get_win_width() const
    {
        return nx*pxPerBlock;
    }

    int get_win_height() const
    {
        return ny*pxPerBlock;
    }    

    int px_to_block(int px) const
    {
        return px/pxPerBlock;
    }

    int py_to_block(int py) const
    {
        return py/pxPerBlock;
    }

    int block_to_px(int bx) const
    {
        return bx*pxPerBlock;
    }

    int block_to_py(int by) const
    {
        return by*pxPerBlock;
    }

    int get_map_index(int x, int y) const
    {
        return y*nx + x;
    }

    int get_x(int idx) const
    {
        return idx%nx;
    }

    int get_y(int idx) const
    {
        return idx/nx;
    }

    bool freespace(double x, double y) const
    {
        return !data[get_map_index(round(x), round(y))];
    }

    bool freespace(int idx) const
    {
        return !data[idx];
    }

    void draw()
    {
        glColor3f(0, 1, 0);
        glBegin(GL_QUADS);
        for (int x = 0; x < nx; ++x)
        {
            for (int y = 0; y < ny; ++y)
            {
                if (!freespace(x, y))
                {
                    glVertex2i( x   *pxPerBlock,  y   *pxPerBlock);
                    glVertex2i((x+1)*pxPerBlock,  y   *pxPerBlock);
                    glVertex2i((x+1)*pxPerBlock, (y+1)*pxPerBlock);
                    glVertex2i( x   *pxPerBlock, (y+1)*pxPerBlock);
                }
            }
        }
        glEnd();
    }
